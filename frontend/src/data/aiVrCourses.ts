export function hasContent(s: AiVrSection): boolean {
  return !!(s.pptUrl || s.videoUrl || s.vrUrl)
}

const S = (title: string, ppt = '', video = '', vr = '', defaultOpen = false): AiVrSection =>
  ({ title, pptUrl: ppt, videoUrl: video, vrUrl: vr, defaultOpen: defaultOpen || undefined })

const C = (title: string, children: AiVrSection[], defaultOpen = false): AiVrChapter =>
  ({ title, children, defaultOpen: defaultOpen || undefined })

